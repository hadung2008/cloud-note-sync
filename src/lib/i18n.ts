export type LangString = keyof typeof translations.vi;

export const t = (lang: 'vi' | 'en', key: LangString, params?: Record<string, string | number>) => {
  let str = translations[lang][key] || translations.vi[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
};
